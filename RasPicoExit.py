import utime
from machine import Pin

# ----------------------------------------------------------------
#  定義
# ----------------------------------------------------------------
PIN_LED = Pin(25, Pin.OUT)
PIN_EXIT = Pin(16, Pin.IN, Pin.PULL_DOWN)

# ----------------------------------------------------------------
#  初期化処理
# ----------------------------------------------------------------
def init():
	print ("--- START ---")

# ----------------------------------------------------------------
#  メイン処理
# ----------------------------------------------------------------
def main():
	while True:
		PIN_LED.value(True)
		utime.sleep(0.5)
		PIN_LED.value(False)
		utime.sleep(0.5)

		# EXITピンが通電するとプログラムを終了させる
		if PIN_EXIT.value() == True:
			break;

# ----------------------------------------------------------------
#  終了処理
# ----------------------------------------------------------------
def destroy():
	print ("--- END ---")

# ----------------------------------------------------------------
#  プログラム実行
# ----------------------------------------------------------------
try:
	init()
	main()
	destroy()

except KeyboardInterrupt:
	destroy()

except Exception as ex:
	print ("Exception:" + ex)
	destroy()